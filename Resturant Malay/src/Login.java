    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(isEmpty())
        {
            String name = username.getText();
            String pass = password.getText();
            try {
            Class.forName("com.mysql.jdbc.Driver");
            cn=DriverManager.getConnection("jdbc:mysql://localhost:3306/resturant_malay?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "");
            st=cn.createStatement();
            ResultSet rs = st.executeQuery("select * from login where username = '"+name+"' and password = '"+pass+"'");
            if(rs.next())
            {
                JOptionPane.showMessageDialog(null,"Login Successfully");
                Dashboard dashboard = new Dashboard();
                dashboard.setVisible(true);
                dashboard.pack();
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Invalid User-name OR Password","Warning",2);
                username.setText("");
                password.setText("");
            }
            cn.close();
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,"Database Not connected");
            username.setText("");
            password.setText("");
        }
            
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

